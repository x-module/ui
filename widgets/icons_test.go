func TestIcons(T *testing.T) {
	var ops op.Ops
	go func() {
		w := new(app.Window)
		th := material.NewTheme()
		color := color.NRGBA{R: 1, G: 1, B: 1, A: 255}
		list := layout.List{Axis: layout.Vertical}
		var iconsSlips []string
		for key := range iconList {
			iconsSlips = append(iconsSlips, key)
		}

		for {
			e := w.Event()
			switch e := e.(type) {
			case app.DestroyEvent:
				panic(e.Err)
			case app.FrameEvent:
				gtx := app.NewContext(&ops, e)
				list.Layout(gtx, len(iconsSlips), func(gtx layout.Context, index int) layout.Dimensions {
					return layout.Flex{Axis: layout.Horizontal}.Layout(gtx,
						layout.Rigid(func(gtx layout.Context) layout.Dimensions {
							return material.Label(th, unit.Sp(16), iconsSlips[index]).Layout(gtx)
						}),
						layout.Rigid(func(gtx layout.Context) layout.Dimensions {
							return layout.Spacer{Width: unit.Dp(10)}.Layout(gtx)
						}),
						layout.Rigid(func(gtx layout.Context) layout.Dimensions {
							gtx.Constraints.Min.X = 100
							icon, _ := widget.NewIcon(iconList[iconsSlips[index]])
							return icon.Layout(gtx, color)
						}),
					)
				})
				e.Frame(gtx.Ops)
			}
		}
	}()
	app.Main()
}
